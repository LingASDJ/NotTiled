package com.mirwanda.nottiled;

public class terrain
{
	private String name;
	private int tile;


	public void setName(String name)
	{
		this.name = name;
	}

	public String getName()
	{
		return name;
	}

	public void setTile(int tile)
	{
		this.tile = tile;
	}

	public int getTile()
	{
		return tile;
	}}
